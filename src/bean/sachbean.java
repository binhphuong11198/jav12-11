package bean;

public class sachbean {
	private String masach;
	private String tensach;
	private String giaban;
	private String mota;
	private String anhbia;
	private String ngaycapnhap;
	private String soluongton;
	private String macd;
	private String manxb;
	public sachbean(String masach, String tensach, String giaban, String mota, String anhbia, String ngaycapnhap,
			String soluongton, String macd, String manxb) {
		super();
		this.masach = masach;
		this.tensach = tensach;
		this.giaban = giaban;
		this.mota = mota;
		this.anhbia = anhbia;
		this.ngaycapnhap = ngaycapnhap;
		this.soluongton = soluongton;
		this.macd = macd;
		this.manxb = manxb;
	}
	public String getMasach() {
		return masach;
	}
	public void setMasach(String masach) {
		this.masach = masach;
	}
	public String getTensach() {
		return tensach;
	}
	public void setTensach(String tensach) {
		this.tensach = tensach;
	}
	public String getGiaban() {
		return giaban;
	}
	public void setGiaban(String giaban) {
		this.giaban = giaban;
	}
	public String getMota() {
		return mota;
	}
	public void setMota(String mota) {
		this.mota = mota;
	}
	public String getAnhbia() {
		return anhbia;
	}
	public void setAnhbia(String anhbia) {
		this.anhbia = anhbia;
	}
	public String getNgaycapnhap() {
		return ngaycapnhap;
	}
	public void setNgaycapnhap(String ngaycapnhap) {
		this.ngaycapnhap = ngaycapnhap;
	}
	public String getSoluongton() {
		return soluongton;
	}
	public void setSoluongton(String soluongton) {
		this.soluongton = soluongton;
	}
	public String getMacd() {
		return macd;
	}
	public void setMacd(String macd) {
		this.macd = macd;
	}
	public String getManxb() {
		return manxb;
	}
	public void setManxb(String manxb) {
		this.manxb = manxb;
	}
	
}
